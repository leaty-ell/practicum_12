import keyword  # Импортируем модуль с ключевыми словами Python

name = input("Введите имя переменной: ")

# Проверяем три условия:
# 1. Не является ли имя ключевым словом
# 2. Начинается ли с цифры
# 3. Содержит ли только разрешенные символы (a-z, A-Z, 0-9, _)

if (not keyword.iskeyword(name)      # Проверка на ключевое слово
    and name[0].isalpha()           # Первый символ - буква
    and name.replace("_", "").isalnum()):  # Остальные символы - буквы/цифры
    print("Да, это допустимое имя")
else:
    print("Нет, это недопустимое имя")
