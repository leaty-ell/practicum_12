def num_to_words(n):
    units = ["", "один", "два", "три", "четыре", "пять",
             "шесть", "семь", "восемь", "девять"]
    teens = ["десять", "одиннадцать", "двенадцать", "тринадцать",
             "четырнадцать", "пятнадцать", "шестнадцать",
             "семнадцать", "восемнадцать", "девятнадцать"]
    tens = ["", "", "двадцать", "тридцать", "сорок", "пятьдесят",
            "шестьдесят", "семьдесят", "восемьдесят", "девяносто"]
    hundreds = ["", "сто", "двести", "триста", "четыреста",
                "пятьсот", "шестьсот", "семьсот", "восемьсот", "девятьсот"]

    def three_digits(n):
        s = ""
        h = n // 100
        t = (n % 100) // 10
        u = n % 10
        if h > 0:
            s += hundreds[h] + " "
        if t == 1:
            s += teens[u] + " "
        else:
            if t > 1:
                s += tens[t] + " "
            if u > 0:
                s += units[u] + " "
        return s.strip()

    result = ""
    if n >= 1_000_000:
        result += three_digits(n // 1_000_000) + " миллион "
        n %= 1_000_000
    if n >= 1000:
        thousands = n // 1000
        words = three_digits(thousands).replace("один", "одна").replace("два", "две")
        result += words + " тысяча "
        n %= 1000
    if n > 0:
        result += three_digits(n)
    return result.strip()

